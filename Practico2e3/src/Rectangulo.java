
public class Rectangulo {
	
	double base;
	double altura;
	
	public Rectangulo(int x, int y){
		base = x;
		altura =y;
	}

	public double getBase() {
		return base;
	}

	public void setBase(double base) {
		this.base = base;
	}

	public double getAltura() {
		return altura;
	}

	public void setAltura(double altura) {
		this.altura = altura;
	}

}
